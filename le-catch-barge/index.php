		      <p>Le CatchBarge est une installation destinée aux vitrines des lieux et conçue pour attirer le chaland, d’où le nom de Catch Barge: Attrape Chaland en français.</p>
           
		    
		      <p>Le CatchBarge de notre lieu permet de piloter un petit train en changeant son trajet via l’activation des aiguillages et en modulant sa vitesse très simplement depuis une interface sur un téléphone mobile.</p>
		      <div>
                <div id="ftn1">
                  <p><a href="#_ftnref1" name="_ftn1" title=""> </a>Le CatchBarge est un hommage  au “Tech Model Railroad Club” (TMRC), le club de modélisme ferroviaire que des élèves du Massachusetts Institute of Technology (MIT) fondèrent en 1949.<br>
                    Ses membres tous passionnés par l’électronique construisirent sur leur temps libre une gigantesque maquette à échelle réduite d’un réseau ferroviaire. Actionner des aiguillages manuellement était bien trop simple pour ces élèves ingénieurs, qui ne tardèrent pas à automatiser cette tâche. <br>
                  Au fur et à mesure du déploiement de la maquette les membres du TMRC l’ont perfectionnée, expérimentant ainsi les rudiments de la programmation. C’est dans ce club que va naître le terme de « hacker », philosophie tournée vers l’expérimentation pratique et le détournement technologique pour arriver à ses fins.</p>
